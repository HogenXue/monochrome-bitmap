/*
u8PicNameOut    输出的bmp路径及名字，返回值，如果pcPtnBuf为NULL时，则返回bmp文件
iWidth          要转换的bmp图片宽度，1-4096
iHeight         要转换的bmp图片高度，1-4096
pcPtnBuf[]      输入的图片buffer地址，可为NULL，为NULL时生成bmp图片，返回的名称在u8PicNameOut
int iBmpBit     要转换的bit数，3-表示输出8bit的bmp，即RGB， 6-表示输出16bit的bmp，RRGGBB
BYTE u8R 
BYTE u8G
BYTE u8B
*/
int generateBmpFunc(char bitset[],const char* filename, int width, int height)
{
    FILE* file = fopen(filename,"wb");
    if(!file) return -1;
    // save bitmap file headers
    BITMAPFILEHEADER fileHeader;
    BITMAPINFOHEADER * infoHeader;
    infoHeader = (BITMAPINFOHEADER*) malloc(sizeof(BITMAPINFOHEADER) );
    RGBQUAD bl = {0,0,0,0};  //black color
    RGBQUAD wh = {0xff,0xff,0xff,0xff}; // white color


    fileHeader.bfType      = 0x4d42;
    fileHeader.bfSize      = 0;
    fileHeader.bfReserved1 = 1;
    fileHeader.bfReserved2 = 1;
    fileHeader.bfOffBits   = sizeof(BITMAPFILEHEADER) + (sizeof(BITMAPINFOHEADER)) + 2*sizeof(RGBQUAD); 

    infoHeader->biSize          = (sizeof(BITMAPINFOHEADER) );
    infoHeader->biWidth         = width;    
    infoHeader->biHeight        = height;
    infoHeader->biPlanes        = 1;
    infoHeader->biBitCount      = 1;
    infoHeader->biCompression   = 0; //no compression needed
    infoHeader->biSizeImage     = 0;
    infoHeader->biXPelsPerMeter = 0;
    infoHeader->biYPelsPerMeter = 0;
    infoHeader->biClrUsed       = 2;
    infoHeader->biClrImportant  = 2;

    fwrite((char*)&fileHeader, sizeof(fileHeader),1,file); //write bitmapfileheader
    fwrite((char*)infoHeader, (sizeof(BITMAPINFOHEADER)),1,file); //write bitmapinfoheader
    fwrite((char*)&wh,sizeof(wh),1,file); //write RGBQUAD for black
    fwrite((char*)&bl,sizeof(bl),1,file); //write RGBQUAD for white

    // convert the bits into bytes and write the file
    int offset, numBytes = ((width + 31) / 32) * 4;
    BYTE* bytes = (BYTE*) malloc(numBytes * sizeof(BYTE));

    for(int y=height - 1; y>=0; --y)
    {
        offset = y * width;
        memset(bytes, 0, (numBytes * sizeof(BYTE)));
        for(int x=0; x<width; ++x)
            if(bitset[offset++]){
                bytes[x / 8] |= 1 << (7 - x % 8);
        };
        fwrite((const char *)bytes, numBytes,1,file);
    };
    free(bytes);
    fclose(file); 
}


